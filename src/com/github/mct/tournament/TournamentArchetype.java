package com.github.mct.tournament;
/**
 * TournamentArchetype contains an enumeration of the SubTournaments possible.
 * SHORT: A short-weapon tournament.
 * MEDIUM: A medium-weapon tournament.
 * LONG: A long-weapon tournament.
 * WILD: An all-weapon tournament.
 *
 * @author Gavin Mullis
 *
 */
public enum TournamentArchetype {
    SHORT, MEDIUM, LONG, WILD
}
